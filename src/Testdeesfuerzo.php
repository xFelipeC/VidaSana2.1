            <h2>¿Qué es un Test de Esfuerzo?</h2>
            <p>Este es un examen que evalúa tu condición cardiovascular durante un ejercicio. Este test permite analizar tu resistencia física, siendo útil para diagnosticar síntomas, pronosticar enfermedades cardiacas y valorar la evolución de aquellas afecciones que ya han sido diagnosticadas.</p>
            <h2>¿En qué consiste el Test de Esfuerzo?</h2>
            <p>Para este examen diagnóstico se posicionan parches adhesivos en el pecho, algunas veces también en brazos y piernas, los que miden la actividad eléctrica de tu corazón mientras realizas la actividad física. <br>
                            <br>
                Estos ejercicios consisten en caminar en una cinta de correr, también conocida como trotadora eléctrica, o pedalear en una bicicleta fija mientras el especialista evalúa el ritmo cardiaco.</p>
            <h2>¿Cómo me preparo para el examen?</h2>
            <h4>Para tomar el test de esfuerzo no es necesario que estés en ayuno y debes tener una orden médica que indique la necesidad de su realización, además sigue estas indicaciones:</h4>
            
            <ul>
            <li>No fumes ni bebas el día del examen.</li>
            <li>Si estás tomando medicamentos no interrumpas el tratamiento, a menos que sea una indicación de tu médico tratante.</li>
            <li>Lleva ropa cómoda, zapatillas y una toalla pequeña.</li>
            <li>Si tienes alguna enfermedad crónica debes avisar al equipo que toma el test.</li>
            <li>Estar en el peso máximo permitido. Conoce dónde lo puedes tomar aquí abajo.</li>
                </ul>
            <h2>¿Tiene algún riesgo hacer una prueba de esfuerzo?</h2>
            <p>La prueba de esfuerzo suele ser segura, dado que el paciente es monitoreado y controlado por el médico que se encuentra presente. <br>

                No obstante, si apareciera algún problema, el paciente deberá ser tratado de inmediato por personal médico disponible en la sala. Siempre existe el riesgo, aunque sea bajo, de desencadenar un infarto al miocardio. <br>

                Cabe destacar que ante la duda de un embarazo o si la mujer está amamantando, es fundamental informar al médico para evaluar las posibilidad de realizar la prueba. <br>

                “Existen diversas contraindicaciones para efectuar una prueba provocadora de isquemia, siendo las más importantes, cuando se presenta el paciente con un cuadro agudo, con inestabilidad, con dolores en reposo, con hipotensión arterial, con severas alteraciones del electrocardiograma, entre otras”, advierte el cardiólogo. <br>

                .Por lo anterior, el cardiólogo que hará la prueba puede decidir no hacer el examen, ante circunstancias como las descritas.</p>


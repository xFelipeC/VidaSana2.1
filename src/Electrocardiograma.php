            <h1>Electrocardiograma (ECG)</h1>
            <p>El electrocardiograma es una prueba médica que registra la actividad eléctrica del corazón a lo largo del tiempo. Es una herramienta fundamental en la cardiología moderna.</p>
            <br>
            <h2>¿Qué es un Electrocardiograma?</h2>
            <p>Un electrocardiograma (ECG) es un procedimiento simple, indoloro y rápido que registra la actividad eléctrica de su corazón. Cada vez que el corazón late, una señal eléctrica circula a través de él. La señal activa las cuatro cámaras de su corazón para que se contraigan (aprieten) al ritmo correcto para que su corazón pueda bombear sangre a su cuerpo. <br>
            <br>

                El registro del ECG de estas señales se ven como líneas onduladas. Su profesional de la salud puede leer estas líneas para buscar actividad cardíaca anormal que pueda ser un signo de enfermedad o daño cardíaco. <br>
                <br>

                 <p>Un electrocardiograma puede mostrar: <br></p> 

                <ul class="lista">
                   <li>Qué tan rápido late su corazón</li>
                   <li>Si el ritmo de su corazón es constante o irregular</li>
                   <li>La fuerza y la sincronización de las señales eléctricas que pasan por cada parte de su corazón</li>
               </ul>
                En ocasiones, la información de un electrocardiograma puede ayudar a medir el tamaño y la posición de las cavidades del corazón. <br>
                <br>
                Un electrocardiograma suele ser la primera prueba que se hace si tiene signos de una afección cardíaca. Se puede realizar en el consultorio de su profesional de la salud, en una clínica ambulatoria, en un hospital antes de la cirugía o como parte de otra examen cardíaco llamado prueba de esfuerzo. <br>
                <br>

                En los estados Unidos, un ECG también se conoce como EKG. EKG se basa en la ortografía alemana, elektrokardiogramm. EKG puede utilizarse sobre ECG para evitar confusiones con una prueba que mide las ondas cerebrales llamada EEG (electroencefalograma). <br>
                <br>

                Nombres alternativos: EKG, monitor Holter, electrocardiografía ambulatoria, electrocardiogramas continuos, monitores de eventos transtelefónicos</p> <br>
                <br>
            <h2>¿Para qué se usa?</h2> 

            <p>El electrocardiograma se usa para ayudar a diagnosticar y vigilar muchos tipos de enfermedades del corazón y su tratamiento. Estas afecciones incluyen:</p> 


            <ul>
            <li>Arritmia</li>
            <li>Cardiomiopatía</li>
            <li>Enfermedad de las arterias coronarias</li>
            <li>Ataque cardiaco</li>
            <li>Insuficiencia cardiaca</li>
            <li>Enfermedades de las válvulas del corazón</li>
            <li>Defectos cardiacos congénitos</li>
        </ul>

        <p>Los electrocardiogramas se utilizan principalmente para personas con síntomas o diagnóstico de problemas cardíacos. No suelen usarse para evaluar a personas sin síntomas, a menos que tengan un mayor riesgo de desarrollar una enfermedad cardíaca. Un profesional de la salud puede evaluar el riesgo y determinar la necesidad de un electrocardiograma, y en algunos casos, recomendar la consulta con un cardiólogo.</p>
            <h2>¿Por qué necesito un electrocardiograma?</h2>
            <p>Usted puede necesitar un electrocardiograma si tiene síntomas de una enfermedad del corazón, incluyendo:</p>

            <ul>
            <li>Dolor de pecho</li>
            <li>Latidos cardíacos rápidos o irregulares</li>
            <li>Dificultad para respirar</li>
            <li>Mareos</li>
            <li>Cansancio</li>
            <li>Una disminución en su capacidad para hacer ejercicio</li>
        </ul>


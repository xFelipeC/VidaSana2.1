            <h1>Holter de Ritmo</h1>
            <p>El Holter de ritmo es un dispositivo que monitoriza la actividad eléctrica del corazón de manera continua durante 24 horas o más, permitiendo detectar irregularidades en el ritmo cardíaco.</p>
            <h2>¿Qué es un Holter de Ritmo?</h2>
            <p>El Holter de ritmo, también conocido como monitor Holter, es un dispositivo portátil que registra la actividad eléctrica del corazón de forma continua durante un período de tiempo extendido, generalmente 24 a 48 horas.</p>
            <h2>¿Cómo saber qué se necesita un Holter?</h2>
            <p>El Holter se suele utilizar en pacientes con sospecha de arritmia cardíaca. Sin embargo, el médico puede recomendarlo en pacientes que presenten síntomas como:</p>  

            <ul>
            <li>Desmayos sin causa aparente.</li>
            <li>Palpitaciones irregulares.</li>
            <li>Cansancio fuera de lo normal.</li>
            <li>Frecuencia cardíaca baja o alta.</li>
            <li>Dolor en el pecho.</li>
            <li>Falta de aire.</li>
            <li>Aturdimiento.</li>
            <li>Después de un ataque cardíaco.</li>
            <li>Al comenzar un medicamento para el corazón.</li>
        </ul>

            <h2>¿Se necesita preparación previa para realizar el Holter?</h2>
            <div class="holterderitmo">
                <img src="../img/holterderitmo.webp" alt="Vida Sana Logo" width="900" height="600">
            </div>
            <p>Para la realización del examen Holter cardíaco no existe preparación alguna, el médico procederá a encender el aparato portátil e indicará cómo colocar los electrodos y qué hacer en caso de que se caiga alguno. <br>
                    <br>
                Lo más recomendable es asearse antes de realizarse el examen, ya que durante la colocación del Holter no se puede mojar el dispositivo; también afeitarse la zona del tórax para evitar incomodidad a la hora de colocar los electrodos, y en caso de alergia por alguna cinta o adhesivo consultar al médico. <br>
                    <br>
                Existen dispositivos personales, como relojes inteligentes, que te permiten llevar un registro del electrocardiograma, mide la actividad eléctrica del corazón. Consulta a tu médico si ésta opción sería válida para ti. </p>

             <h2>¿Cómo funciona el Holter?</h2>  
             
             <p>Para la colocación del monitor Holter tiene que haber una cita programada en el consultorio del cardiólogo. En el consultorio, el médico procederá a colocar unos parches conductores conocidos como electrodos, estos se colocarán en el pecho, específicamente en el tórax. Luego se conectarán mediante unos cables hacia una grabadora digitalizada que suele tener el tamaño de un teléfono móvil. <br>
                    <br>
                Una vez colocado el dispositivo Holter, el paciente lo llevará consigo en una bolsa o bolsillo que la llevará puesto alrededor de su cintura o cuello en el plazo indicado por su médico. El Holter funciona con baterías, y su duración es de un estudio, es decir, la batería debe ser cambiada cada vez que se realice un nuevo estudio. <br>
                    <br>
                Una vez colocado, el paciente deberá mantener el Holter colocado en todo momento. Asimismo, debe mantener el área pecho seca y limpia para evitar cualquier daño del dispositivo.</p>
       
            <h2>¿Qué debo hacer mientras tenga puesto el Holter?</h2>

            <div class="holterritmo2">
                <img src="../img/holterderitmo2.webp" alt="Vida Sana Logo" width="800" height="600">
                <p>qué hacer mientras se tiene el Holter puesto</p>

            <p>El paciente que lleva el monitor Holter debe llevar un registro diario de las actividades que realiza y cómo se siente al respecto. Así, permite darle mayor información al médico y realizar una evaluación más detallada. Toma en cuenta que se debe tener una rutina cotidiana sin ningún tipo de limitación (a menos que su médico indique lo contrario), ya que el objetivo es ver qué comportamiento mantiene su corazón en sus actividades rutinarias. <br>
                    <br>
                Si el paciente llegara a presentar algún síntoma durante el tiempo que lleva el monitor Holter, como dolor en el pecho, palpitaciones irregulares, mareos o falta de aire, es necesario que apunte la hora y pueda informarle a su médico; esto permitirá correlacionar los datos con los síntomas causados durante su tiempo utilizando el Holter.</p> <br>

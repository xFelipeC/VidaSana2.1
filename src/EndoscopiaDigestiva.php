    <h2>Interpretación</h2>
     <p>La interpretación depende del cuadro clínico y de los exámenes complementarios del paciente, por lo que varía caso a caso.</p>
     <h2>Signos de alarma</h2>
     <p>Si bien no es del examen propiamente tal, siempre se debe considerar como antecedente cuando se estudia a un paciente con insuficiencia renal aguda, ya que las soluciones con fosfatos, en proceso de retirarse del mercado, son nefrotóxicas.</p>
     <h2>Riesgos</h2>
     <p>Los riesgos provienen de la inestabilidad hemodinámica (IAM, disección aórtica, ACV, depresión respiratoria, etc.), del riesgo de perforación y de la posible hemorragia que se podría producir.</p>

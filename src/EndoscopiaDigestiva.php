    <p>Los resultados son entregados en un informe médico en donde se adjuntan fotos, se describen los hallazgos y se plantea una hipótesis diagnóstica.</p>
    <h2>Interpretación</h2>
    <p>La interpretación dependerá del cuadro clínico del paciente y de los exámenes complementarios.</p>
    <h2>Signos de alarma</h2>
    <p>Cualquier lesión sospechosa en la endoscopia es biopsiada de inmediato. Se debe recordar la alta tasa de incidencia de cáncer gástrico en nuestro país. El esófago de Barret y el riesgo de adenocarcinoma esofágico es una entidad poco frecuente en nuestro medio. <br>

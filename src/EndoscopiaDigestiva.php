        <section id="more-info" class="hidden">
            <h2>¿Qué es una Endoscopia Digestiva Alta/Baja?</h2>
            <p>La endoscopia digestiva alta permite visualizar el esófago, el estómago y la parte superior del intestino delgado (duodeno), mientras que la endoscopia digestiva baja (colonoscopia) se utiliza para examinar el colon y el recto.</p>
            <h2>¿Cómo se realiza?</h2>
            <p>Durante la endoscopia digestiva alta, se introduce un endoscopio flexible a través de la boca hasta el tracto digestivo superior. Para la endoscopia digestiva baja, el endoscopio se introduce a través del recto. Ambos procedimientos se realizan bajo sedación.</p>
            <h2>¿Por qué es importante?</h2>
            <p>Estas pruebas son esenciales para diagnosticar problemas digestivos como úlceras, inflamaciones, tumores y sangrado interno. También pueden ser usadas para realizar biopsias y tratar ciertas condiciones.</p>

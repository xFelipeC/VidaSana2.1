            <h1>Endoscopia Digestiva Alta/Baja</h1>
            <p>La endoscopia digestiva alta/baja es un procedimiento que permite a los médicos visualizar el interior del tracto digestivo mediante un endoscopio, ayudando a diagnosticar y tratar diversas afecciones.</p>
               
            <h2>¿Qué es una Endoscopia Digestiva Alta?</h2>
            <p>La endoscopía digestiva alta es un procedimiento que permite revisar la orofaringe, esófago (esofagoscopia), estómago (gastroscopía) y duodeno proximal utilizando un instrumento llamado videoendoscopio. El procedimiento permite observar directamente el interior del tubo digestivo, tomar fotos, grabar videos, tomar muestras o realizar algún procedimiento. La endoscopia es un procedimiento diagnóstico y terapeutico</p> <br>
            <h2>¿Cómo se realiza?</h2>
            <p>Se requiere que el paciente se encuentre en ayuno. Se puede colocar un anestésico local en la faringe para disminuir las molestias. El paciente es sedado y el médico introduce el videoscopio por la boca y avanza hacia caudal. En general es necesario insuflar aire para separar las paredes del tubo digestivo y poder visualizar correctamente todas las estructuras.</p>
            <h2>Contraindicaciones</h2>
            <p>las contraindicaciones tienen su sustrato fisiopatológico en los riesgos de la sedación y de la perforación del tubo digestivo (recordemos que el procedimiento implica la insuflación de aire con el respectivo aumento de la presión luminal)</p> <br>

            <div>
        <p>Indicaciones Absolutas para Procedimientos:</p>
        <ul>
            <li>Shock.</li>
            <li>Disnea severa con hipoxemia.</li>
            <li>Coma (intubación).</li>
            <li>Convulsiones.</li>
            <li>Perforación aguda del tracto digestivo.</li>
        </ul>
    </div>
    <div>
        <p>Indicaciones Relativas para Procedimientos:</p>
        <ul>
            <li>Coagulopatía.</li>
            <li>Divertículo de Zenker.</li>
            <li>Estenosis del esófago superior.</li>
            <li>Isquemia miocárdica.</li>
            <li>Aneurisma de la aorta torácica.</li>
        </ul>
    </div>

    <p>Los riesgos son los asociados a la sedación y a la perforación del tubo digestivo. Es por ello que se encuentra contraindicada cuando hay inestabilidad hemodinámica o el paciente es susceptible de ella, al igual que cuando hay riesgo de perforación (o ya existe):</p>
     
    <h2>¿Qué es una Endoscopia Digestiva Baja?</h2>
    <p>Técnica diagnóstica y terapéutica que consiste en la introducción de una cámara (videoscopio) por el ano para recorrer el intestino grueso de distal a proximal. También se puede ver la porción terminal del ileon</p>
        <h2>¿Cómo se realiza?   </h2>
        <p>El paciente requiere preparación especial ya que se necesita que el colon este limpio, sin restos solidos ni liquidos que impidan observar bien la mucosa. La limpieza la podemos agrupar en aquella que utiliza la via oral y la que utiliza la via anal. Ambas deben ser combinadas ya que de forma aislada no son eficientes. Las que utilizan la via anal son especialmente útiles cuando el paciente posee una colostomía con cierre del muñon distal (operación de Hartman). <br>
            <br>
La preparación de la via oral corresponde a una dieta sin residuos que se debe instaurar 4 días antes del examen y a algunas soluciones que pueden comenzar desde minutos antes del examen a dias antes. <br>
            <br>
La preparación por via anal corresponde a enemas, irrigaciones, lavados y soluciones. Las soluciones disponibles son multiples y su uso varía según patologías concomitantes que tenga el paciente (insuficiencia renal crónica, insuficienca hepática, etc.) <br>
     <br>
Los métodos a usar dependen del especialista. <br>
            <br>
Una vez que el paciente se encuentra preparado es sedado y se posiciona en decúbito lateral izquierdo. Él medico introduce el videoscopio por el ano y avanza hacia cefálico. El colon se insufla con aire para distender las paredes y observarlo mejor. </p>
        <h2> Contraindicaciones </h2>
        <p>Tienen su sustrato fisiopatológico en la sedación con su riesgo hemodinámico (sumado a un posible reflejo vagal) y en el aumento de la presion luminal con su riesgo de perforación que ello supone (recordemos que el colon se inslufla). Por lo tanto, todas las contraindicaciones pueden ser vistas desde esos dos puntos</p> <br>

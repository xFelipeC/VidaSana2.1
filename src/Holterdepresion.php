            <h1>Holter de Presión</h1>
            <p>El Holter de presión es una herramienta diagnóstica que monitorea la presión arterial del paciente durante un período de 24 horas o más, permitiendo una evaluación detallada de la variabilidad de la presión arterial a lo largo del día.</p>
            <h2>¿Qué es un Holter de Presión?</h2>
            <p>El Holter de la presión arterial es un examen diagnóstico que se realiza en cardiología para medir la presión arterial en un sujeto a lo largo de un espacio de tiempo prolongado, normalmente de 24 horas. <br>

              El objetivo es controlar la presión arterial del paciente y localizar en qué momentos o con qué actividades el paciente presenta cambios bruscos de tensión. <br>

              Existen dos tipos de exámenes Holter, el que mide la presión arterial y el que mide el ritmo cardíaco. Esta prueba obtiene su nombre del biofísico Norman Golter, quien el año 1949 estableció los cimientos teóricos de la monitorización cardíaca.</p>
                 
            <h2>¿En qué consiste?</h2>
            <p>El Holter de presión arterial consiste en aplicar un manguito con dispositivo de medida para ir recogiendo los niveles de presión arterial durante todo el día, incluso durante el sueño si es necesario. <br>

            El manguito se coloca en el brazo no dominante del paciente, e irá conectado mediante cables a un pequeño equipo, similar en forma y tamaño a un radiocasete, que puede agarrarse en el cinturón o llevar en un bolsillo. <br>

            Este dispositivo puede ocultarse perfectamente bajo la ropa y el paciente deberá llevarlo puesto todo el tiempo y realizar sus actividades normales mientras lo lleve. El dispositivo lee la presión cada 15 o 20 minutos durante los periodos de actividad y cada 30 minutos durante el sueño. <br>

            Cuando acaba el examen, se retira el equipo y se leen las frecuencias registradas mediante un ordenador. el especialista cardiólogo suele extraer una gráfica a partir de estas frecuencias para su observación.</p> <br>


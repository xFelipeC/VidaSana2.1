            <p>El Holter de presión arterial recoge los niveles de presión arterial durante todo el día.</p>

            <h2>¿Por qué se realiza?</h2>
            <p>El objetivo del Holter de presión arterial es realizar el seguimiento de la presión arterial del sujeto a lo largo de su jornada habitual:</p> 
             <br>
             <ul>
            <li>Se suele realizar en pacientes que se sospecha pueden presentar hipertensión arterial pero no ha podido confirmarse en el momento de la consulta o para llevar un seguimiento de pacientes hipertensos de difícil control o que toman medicación que pueda influir en la presión arterial.</li>
            <li>También se realiza para valorar la efectividad del tratamiento para la tensión del paciente.</li>
            <li>En aquellos pacientes que presenten hipertensión episódica, o una presión arterial muy variable, también se realiza este seguimiento para ver en qué momento o qué actividades perjudican su presión arterial.</li>
            <li>No solo en casos de hipertensión, también los pacientes con hipotensión arterial pueden necesitar un seguimiento con Holter de presión.</li>
        </ul>

            <h2>Preparación para el Holter de presión arterial</h2>

            <p>El paciente no suele requerir preparación para esta prueba. ya que el objetivo es registrar su actividad de un día normal. Por tanto, deberá realizar las mismas actividades que normalmente le ocupan, tomar su medicación habitual y evitar ejercicio físico intenso o actividades que le provoquen nervios y estrés inusuales. <br>

                También es posible que el cardiólogo pida al paciente que anote los medicamentos que va tomando o las actividades que ha ido realizando durante el día del examen.</p>

            
